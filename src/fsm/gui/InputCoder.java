    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        StringBuilder pattern = new StringBuilder();
        for (int i = 0; i < (Integer)jSpinner1.getValue();i++) {
            pattern.append('$');
        }
        pattern.append(jTextField1.getText());
        if (jRadioButton1.isSelected()) {
            for (int i = 0; i < 2*(Integer)jSpinner1.getValue();i++) {
                pattern.append('$');
            }
            while (jTextField2.getText().length()>pattern.length()-2*(Integer)jSpinner1.getValue()) {
                pattern.append('$');
            }
        }
        if (jRadioButton1.isSelected()) {
            ((DefaultTableModel)jTable1.getModel()).setNumRows(Math.max(jTextField1.getText().length()+(Integer)jSpinner1.getValue(),jTextField2.getText().length()));
            
        } else {
            ((DefaultTableModel)jTable1.getModel()).setNumRows(jTextField2.getText().length());
            
        }
        StringBuilder code = new StringBuilder();
        for (int i = 0; i < jTable1.getModel().getRowCount();i++) {
            char c = (jTextField2.getText().length()>i?jTextField2.getText().charAt(i):'$');
            String s;
            if (i>pattern.length()) s = "";
            else s = pattern.substring(i, Math.min(i+2*(Integer)jSpinner1.getValue()+1+(jRadioButton1.isSelected()?0:1),pattern.length()));
            jTable1.setValueAt("X("+c+", "+s+")", i, 0);
            StringBuilder r = new StringBuilder();
            for (int j = 0; j < s.length();j++) {
                if (c == s.charAt(j)) r.append('1');
                else r.append('0');
            }
            jTable1.setValueAt(r.toString(), i, 1);
            code.append(r.toString());
        }
        jTextField3.setText(code.toString());
    }//GEN-LAST:event_jButton1ActionPerformed

